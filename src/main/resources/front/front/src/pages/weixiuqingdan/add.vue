<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            baoxiudanhao : false,
            baoxiubiaoti : false,
            shigongtupian : false,
            weixiujine : false,
            guzhangbaogao : false,
            weixiuxiangqing : false,
            wangongshijian : false,
            yezhuzhanghao : false,
            yezhuxingming : false,
            sfsh : false,
            shhf : false,
            ispay : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          baoxiudanhao: '',
          baoxiubiaoti: '',
          shigongtupian: '',
          weixiujine: '',
          guzhangbaogao: '',
          weixiuxiangqing: '',
          wangongshijian: '',
          yezhuzhanghao: '',
          yezhuxingming: '',
          ispay: '',
        },
        rules: {
          baoxiudanhao: [
          ],
          baoxiubiaoti: [
          ],
          shigongtupian: [
          ],
          weixiujine: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          guzhangbaogao: [
          ],
          weixiuxiangqing: [
          ],
          wangongshijian: [
          ],
          yezhuzhanghao: [
          ],
          yezhuxingming: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
          ispay: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.wangongshijian = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='baoxiudanhao'){
              this.ruleForm.baoxiudanhao = obj[o];
              this.ro.baoxiudanhao = true;
              continue;
            }
            if(o=='baoxiubiaoti'){
              this.ruleForm.baoxiubiaoti = obj[o];
              this.ro.baoxiubiaoti = true;
              continue;
            }
            if(o=='shigongtupian'){
              this.ruleForm.shigongtupian = obj[o].split(",")[0];
              this.ro.shigongtupian = true;
              continue;
            }
            if(o=='weixiujine'){
              this.ruleForm.weixiujine = obj[o];
              this.ro.weixiujine = true;
              continue;
            }
            if(o=='guzhangbaogao'){
              this.ruleForm.guzhangbaogao = obj[o];
              this.ro.guzhangbaogao = true;
              continue;
            }
            if(o=='weixiuxiangqing'){
              this.ruleForm.weixiuxiangqing = obj[o];
              this.ro.weixiuxiangqing = true;
              continue;
            }
            if(o=='wangongshijian'){
              this.ruleForm.wangongshijian = obj[o];
              this.ro.wangongshijian = true;
              continue;
            }
            if(o=='yezhuzhanghao'){
              this.ruleForm.yezhuzhanghao = obj[o];
              this.ro.yezhuzhanghao = true;
              continue;
            }
            if(o=='yezhuxingming'){
              this.ruleForm.yezhuxingming = obj[o];
              this.ro.yezhuxingming = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('weixiuqingdan/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('weixiuqingdan/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('weixiuqingdan/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('weixiuqingdan/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      shigongtupianUploadChange(fileUrls) {
          this.ruleForm.shigongtupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

