<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            dengjibianhao : false,
            leixing : false,
            churushijian : false,
            beizhu : false,
            yezhuzhanghao : false,
            yezhuxingming : false,
            zhaopian : false,
        },
        type: '',
        userface : '',
        faceMatchFlag: false,
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          dengjibianhao: this.getUUID(),
          leixing: '',
          churushijian: '',
          beizhu: '',
          yezhuzhanghao: '',
          yezhuxingming: '',
          zhaopian: '',
        },
        leixingOptions: [],
        rules: {
          dengjibianhao: [
            { required: true, message: '登记编号不能为空', trigger: 'blur' },
          ],
          leixing: [
          ],
          churushijian: [
          ],
          beizhu: [
          ],
          yezhuzhanghao: [
          ],
          yezhuxingming: [
          ],
          zhaopian: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.churushijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='dengjibianhao'){
              this.ruleForm.dengjibianhao = obj[o];
              this.ro.dengjibianhao = true;
              continue;
            }
            if(o=='leixing'){
              this.ruleForm.leixing = obj[o];
              this.ro.leixing = true;
              continue;
            }
            if(o=='churushijian'){
              this.ruleForm.churushijian = obj[o];
              this.ro.churushijian = true;
              continue;
            }
            if(o=='beizhu'){
              this.ruleForm.beizhu = obj[o];
              this.ro.beizhu = true;
              continue;
            }
            if(o=='yezhuzhanghao'){
              this.ruleForm.yezhuzhanghao = obj[o];
              this.ro.yezhuzhanghao = true;
              continue;
            }
            if(o=='yezhuxingming'){
              this.ruleForm.yezhuxingming = obj[o];
              this.ro.yezhuxingming = true;
              continue;
            }
            if(o=='zhaopian'){
              this.ruleForm.zhaopian = obj[o].split(",")[0];
              this.ro.zhaopian = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            this.userface = res.data.data.touxiang;
            if((json.yezhuzhanghao!=''&&json.yezhuzhanghao) || json.yezhuzhanghao==0){
                this.ruleForm.yezhuzhanghao = json.yezhuzhanghao
            }
            if((json.yezhuxingming!=''&&json.yezhuxingming) || json.yezhuxingming==0){
                this.ruleForm.yezhuxingming = json.yezhuxingming
            }
          }
        });
        this.leixingOptions = "进入,离开".split(',')
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('zhihuimenjin/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {
            if(!this.faceMatchFlag) {
                //人脸校验
                if(!this.ruleForm.zhaopian) {
                    this.$message({
                      message: "请上传人脸图片",
                      type: 'error',
                      duration: 1500
                    });
                    return;
                }
            if(this.ruleForm.zhaopian!=null) {
                this.ruleForm.zhaopian = this.ruleForm.zhaopian.replace(new RegExp(this.$config.baseUrl,"g"),"");
            }
            let params = {
                face1:this.userface.replace("upload/",""),
                face2:this.ruleForm.zhaopian.replace("upload/",""),
            }
            this.$http.get('matchFace', {params: params}).then(res => {
              if (res.data && res.data.code == 0) {
                    if(res.data.score>60) {
                        this.faceMatchFlag = true;
                        this.$message({
                          message: "匹配成功",
                          type: "success",
                          duration: 1500,
                        });
                    } else {
                        this.faceMatchFlag = false;
                        this.$message.error("匹配失败");
                        return;
                    }
                } else {
                  this.faceMatchFlag = false;
                  this.$message.error("匹配失败");
                  return;
              }
            });
            return;
        }

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('zhihuimenjin/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('zhihuimenjin/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('zhihuimenjin/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      zhaopianUploadChange(fileUrls) {
          this.ruleForm.zhaopian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

