<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            baoxiudanhao : false,
            baoxiumingcheng : false,
            tupian : false,
            leixing : false,
            baoxiuneirong : false,
            baoxiuriqi : false,
            yezhuzhanghao : false,
            yezhuxingming : false,
            loudong : false,
            danyuan : false,
            menpaihao : false,
            sfsh : false,
            shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          baoxiudanhao: this.getUUID(),
          baoxiumingcheng: '',
          tupian: '',
          leixing: '',
          baoxiuneirong: '',
          baoxiuriqi: '',
          yezhuzhanghao: '',
          yezhuxingming: '',
          loudong: '',
          danyuan: '',
          menpaihao: '',
        },
        leixingOptions: [],
        rules: {
          baoxiudanhao: [
          ],
          baoxiumingcheng: [
          ],
          tupian: [
          ],
          leixing: [
          ],
          baoxiuneirong: [
          ],
          baoxiuriqi: [
          ],
          yezhuzhanghao: [
          ],
          yezhuxingming: [
          ],
          loudong: [
          ],
          danyuan: [
          ],
          menpaihao: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.baoxiuriqi = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='baoxiudanhao'){
              this.ruleForm.baoxiudanhao = obj[o];
              this.ro.baoxiudanhao = true;
              continue;
            }
            if(o=='baoxiumingcheng'){
              this.ruleForm.baoxiumingcheng = obj[o];
              this.ro.baoxiumingcheng = true;
              continue;
            }
            if(o=='tupian'){
              this.ruleForm.tupian = obj[o].split(",")[0];
              this.ro.tupian = true;
              continue;
            }
            if(o=='leixing'){
              this.ruleForm.leixing = obj[o];
              this.ro.leixing = true;
              continue;
            }
            if(o=='baoxiuneirong'){
              this.ruleForm.baoxiuneirong = obj[o];
              this.ro.baoxiuneirong = true;
              continue;
            }
            if(o=='baoxiuriqi'){
              this.ruleForm.baoxiuriqi = obj[o];
              this.ro.baoxiuriqi = true;
              continue;
            }
            if(o=='yezhuzhanghao'){
              this.ruleForm.yezhuzhanghao = obj[o];
              this.ro.yezhuzhanghao = true;
              continue;
            }
            if(o=='yezhuxingming'){
              this.ruleForm.yezhuxingming = obj[o];
              this.ro.yezhuxingming = true;
              continue;
            }
            if(o=='loudong'){
              this.ruleForm.loudong = obj[o];
              this.ro.loudong = true;
              continue;
            }
            if(o=='danyuan'){
              this.ruleForm.danyuan = obj[o];
              this.ro.danyuan = true;
              continue;
            }
            if(o=='menpaihao'){
              this.ruleForm.menpaihao = obj[o];
              this.ro.menpaihao = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.yezhuzhanghao!=''&&json.yezhuzhanghao) || json.yezhuzhanghao==0){
                this.ruleForm.yezhuzhanghao = json.yezhuzhanghao
            }
            if((json.yezhuxingming!=''&&json.yezhuxingming) || json.yezhuxingming==0){
                this.ruleForm.yezhuxingming = json.yezhuxingming
            }
            if((json.loudong!=''&&json.loudong) || json.loudong==0){
                this.ruleForm.loudong = json.loudong
            }
            if((json.danyuan!=''&&json.danyuan) || json.danyuan==0){
                this.ruleForm.danyuan = json.danyuan
            }
            if((json.menpaihao!=''&&json.menpaihao) || json.menpaihao==0){
                this.ruleForm.menpaihao = json.menpaihao
            }
          }
        });
        this.leixingOptions = "水工,电工,木工".split(',')
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('wuyebaoxiu/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('wuyebaoxiu/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('wuyebaoxiu/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('wuyebaoxiu/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      tupianUploadChange(fileUrls) {
          this.ruleForm.tupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

